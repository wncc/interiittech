	          <h2>Preamble <span></span></h2>
		<p>The Interdisciplinary Sociotechnical Practicum (ISTP) is an optional undergraduate level practicum for third year B Tech students of all disciplines.  It is a part of the Design & Innovation Stream that consists of Reverse Engineering in the 1st year, Design Practicum in the 2nd year and culminates in the Major Technical Practicum in the 4th year.</p>
		<p>The ISTP will expose students to the complex interactions of technology and society. It will equip them with the tools and skills to systematically understand and evaluate these interactions.  On a higher plane, the ISTP is intended to sensitize students with the problems around them in their society and to develop a sense of responsibility by being the solution provider. This is to nurture the qualities required to become an excellent engineer or technocrat who contributes to society in a variety of ways.</p>
		<br />
		<h2>Objectives</h2>
		<ol>
			<li>1.	To analyze and critically evaluate and present data, ideas, arguments, and points of view</li>
			<li>2.	To develop your oral and written communication skills;</li>
			<li>3.	To learn to work with others as a member of a team (these skills are critical to the success of your ISTP goals);</li>
			<li>4.	A project report containing one or more of:</li>
		</ol>
				<ul>
					<li>•	evaluation of the feasibility of proposed technical solutions to the problem in a specific socio-economic context</li>
					<li>•	market analysis of the proposed solution</li>
					<li>•	environmental impact of the proposed solution</li>
					<li>•	 effect of a new technology on the community</li>
					<li>•	experiences with a pilot deployment in case the solution can be deployed using off-the-shelf components</li>
				</ul>
		<p>The overall objective of this course is to make a positive contribution to your host community.  Please do not lose sight of this when evaluating what you will get out of the ISTP.  We encourage you to treat your project with the respect and sensitivity it deserves, as these are real issues that will impact real people long after you have graduated. </p>


